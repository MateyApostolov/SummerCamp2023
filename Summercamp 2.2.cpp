#include <iostream>
using namespace std;

int main () {
    int n1, n2, n3;
    cout << "Number1 is ? ";
    cin >> n1;
    cout << "Number2 is ? ";
    cin >> n2;
    cout << "Number3 is ? ";
    cin >> n3;
    cout << n1 << " + " << n2 << " = " << n1 + n2 << endl;
    cout << n2 << " - " << n3 << " = " << n2 - n3 << endl;
    cout << n1 << " * " << n3 << " = " << n1 * n3 << endl;
    cout << n3 << " / " << n2 << " = " << n3 / n2 << endl;

    return 0;
}
